package day04_operator;

public class StringConcetenation {

	public static void main(String[] args) {
		String make = "Tesla";
		String model = "Model X";
		
		System.out.println(make);
		System.out.println(model);
		System.out.println("My next car will be ");
		System.out.println("My next car will be "+make);
		System.out.println("My next car will be "+make+model);
		System.out.println("My next car will be "+make+" "+model);
		
		
		String name = "Anh";
		
		System.out.println("My name is "+name);
	}

}
